public class List_Photo_Item {

    public String id;
    public String img;
    public String name;
    public String email;
    public String camera;
    public String site;
    public String phone;


    public List_Photo_Item(String id, String img, String name, String email, String camera, String site, String phone) {
        this.id = id;
        this.img = img;
        this.name = name;
        this.email = email;
        this.camera = camera;
        this.site = site;
        this.phone = phone;
    }
}

